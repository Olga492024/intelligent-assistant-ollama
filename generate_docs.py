def generate_docs(output_dir="src/data/sample_docs", num_docs=50):
    """Генерирует 50 текстовых документов по разным темам ИИ."""
    os.makedirs(output_dir, exist_ok=True)

    doc_count = 0
    topic_keys = list(topics.keys())

    while doc_count < num_docs:
        for topic in topic_keys:
            if doc_count >= num_docs:
                break
            for i, content in enumerate(topics[topic]):
                if doc_count >= num_docs:
                    break
                # Формируем читаемое имя файла
                filename = f"{doc_count+1:02d}_{topic}_{i+1}.txt"
                filepath = os.path.join(output_dir, filename)

                with open(filepath, "w", encoding="utf-8") as f:
                    f.write(f"# {topic.replace('_', ' ').title()}\n\n")
                    f.write(content)

                print(f"Создан файл: {filename}")
                doc_count += 1

    print(f"\nСгенерировано {doc_count} документов в папке '{output_dir}'.")

if __name__ == "__main__":
    generate_docs()